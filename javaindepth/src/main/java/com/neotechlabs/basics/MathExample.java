package com.neotechlabs.basics;

public class MathExample {
    public static void main(String[] args) {

        System.out.println(0.0/0.0);
        System.out.println(Math.sqrt(-2.0));
        System.out.println(Math.sqrt(2.0));
        System.out.println((int)(Math.random() * 5));
        System.out.println(Math.round(21.5));
        System.out.println(Math.ceil(21.1));
        System.out.println(Math.ceil(22));
        System.out.println(Math.floor(21.1));
        System.out.println(Math.ceil(23));
        System.out.println(Math.max(23, 25));
        System.out.println(Math.min(23, 25));

    }
}
